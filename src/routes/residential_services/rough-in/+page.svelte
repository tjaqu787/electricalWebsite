    <h2 class="text-2xl font-semibold mb-2">What is an Electrical Rough-In?</h2>
    <p class="mb-4">The electrical rough-in process involves installing all electrical boxes, wiring, and other infrastructure components before the walls are closed with drywall. This is a critical stage in any renovation, as it lays the groundwork for all future electrical elements, including outlets, switches, light fixtures, and appliances.</p>
    
    <h2 class="text-2xl font-semibold mb-2">Steps Involved in Electrical Rough-In</h2>
    
    <h3 class="text-xl font-semibold mb-2">Planning and Design</h3>
    <p class="mb-4">Work with a licensed electrician to create a detailed plan that considers all electrical needs for the renovated space. This includes the placement of switches, outlets, light fixtures, and special wiring for high-powered appliances.</p>
    
    <h3 class="text-xl font-semibold mb-2">Running Wires</h3>
    <p class="mb-4">Route electrical wires through the conduits and secure them to the boxes. This includes wires for power supply and communication cables like ethernet or telephone lines.</p>
    
    <h3 class="text-xl font-semibold mb-2">Inspection</h3>
    <p class="mb-4">Before the walls are closed up, an inspection by a city building inspector is often required to ensure all installations meet electrical codes and safety standards.</p>
